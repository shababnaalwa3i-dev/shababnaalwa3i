// Scroll trigger helpers
window.scrollToSection = function(id) {
  document.body.classList.remove('nav-open');
  const target = document.getElementById(id);
  if (target) {
    const offset = 80;
    const bodyRect = document.body.getBoundingClientRect().top;
    const elementRect = target.getBoundingClientRect().top;
    const elementPosition = elementRect - bodyRect;
    const offsetPosition = elementPosition - offset;

    window.scrollTo({
      top: offsetPosition,
      behavior: 'smooth'
    });
  }
};

// Interactive header scrolling
window.addEventListener('scroll', () => {
  const header = document.querySelector('.site-header');
  if (window.scrollY > 40) {
    header.classList.add('is-scrolled');
  } else {
    header.classList.remove('is-scrolled');
  }

  // Highlight navigation link corresponding to active section
  const sections = ['home', 'about', 'vision', 'bylaws', 'leadership', 'offices', 'join', 'news', 'contact'];
  const scrollPosition = window.scrollY + 160;

  for (const s of sections) {
    const el = document.getElementById(s);
    if (el) {
      const top = el.offsetTop;
      const height = el.offsetHeight;
      if (scrollPosition >= top && scrollPosition < top + height) {
        document.querySelectorAll('.nav-link').forEach(link => {
          link.classList.toggle('is-active', link.getAttribute('onclick').includes(s));
        });
        break;
      }
    }
  }
});

